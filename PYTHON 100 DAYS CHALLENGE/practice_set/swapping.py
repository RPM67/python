# Python Program - Swap Two Variables in Python

a = int(input("Enter 1st number : "))
b = int(input("Enter 2nd number : "))

print("Before Swapping :-")
print("1st number is",a)
print("2nd number is",b)

temp = a
a = b
b = temp

print("After Swapping :-")
print("1st number is",a)
print("2nd number is",b)