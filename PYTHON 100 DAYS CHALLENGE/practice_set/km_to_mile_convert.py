# Python Program to Convert Kilometers to Miles and vice versa 


km = float(input("Enter the distance in km : "))
mile = km * 0.621371 # 1 km = 0.621371 mile 
print("Distance in mile is",mile)


mile = float(input("Enter the distance in mile : "))
km = mile * 1.609 # 1 mile = 1.609 km 
print("Distance in km is",km)



