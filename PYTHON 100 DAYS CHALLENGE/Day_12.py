frt = "Mango"

print(len(frt)) #prints length of frt string

print(frt[:5]) #prints 5th index value of string frt                  

print(frt[0:4]) #prints sliced part of frt string from index 0-3(4 excluded)

print(frt[-3]) #prints (len(frt)-3)th index value of string frt 

print(frt[-4:-1]) #prints sliced part of frt string from index (len(frt)-4 = 1) to (len(frt)-1 = 4) (last index excluded)

print(frt[4:2])  #reverse order sliced string don't get printed